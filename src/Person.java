public class Person {
    private int id;
    private String name;
    private String age;
    private String sex;
    private String telNum;
    private String address;
    public Person() {
    }
    public Person(String name, String age, String sex, String telNum, String address) {
        this.name = name;
        this.age = age;
        this.sex = sex;
        this.telNum = telNum;
        this.address = address;
    }
    public int getId() {
        return id;
    }
    public void setId(int id) {
        this.id = id;
    }
    public String getName() {
        return name;
    }
    public void setName(String name) {
        this.name = name;
    }
    public String getAge() {
        return age;
    }
    public void setAge(String age) {
        this.age = age;
    }
    public String getSex() {
        return sex;
    }
    public void setSex(String sex) {
        this.sex = sex;
    }
    public String getTelNum() {
        return telNum;
    }
    public void setTelNum(String telNum) {
        this.telNum = telNum;
    }
    public String getAddress() {
        return address;
    }
    public void setAddress(String address) {
        this.address = address;
    }
    @Override
    public String toString() {
        StringBuffer sb = new StringBuffer();
        sb.append("Index").append(this.id).append("#").append("\t");
        sb.append("Name").append(this.name).append("\t\t");
        sb.append("Age").append(this.age).append("\t\t");
        sb.append("Gender").append(this.sex).append("\t\t");
        sb.append("Cell").append(this.telNum).append("\t\t");
        sb.append("Address").append(this.address);
        return sb.toString();
    }
}
